    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String user=this.jTextField1.getText();
        String pass=this.jPasswordField1.getText();
        
        try{
            if(user.equals("") || pass.equals(""))
            {
                JOptionPane.showMessageDialog(this, "Please enter username and password...");
            }
            else
            {
                Stuconnection con=new Stuconnection();
                String s="select * from admin where username='"+user+"' and password='"+pass+"'";
                ResultSet result=con.stm.executeQuery(s);
                if(result.next())
                {
                    this.jProgressBar1.setVisible(true);
                    Thread t=new Thread(this);
                    t.start();
                }
                else
                {
                    JOptionPane.showMessageDialog(this,"Wrong Password ..Enter carefully..");
                    new Adminlogin().setVisible(true);
                }
            }
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

